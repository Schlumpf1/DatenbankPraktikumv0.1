class basketItem:
    def __init__(self,id, itemId, name,preis):
        self.id = id
        self.itemId = itemId
        self.name = name
        self.preis = preis

    def id(self):
        return self.id

    def itemId(self):
        return self.itemId
        
    def name(self):
        return self.name
    
    def preis(self):
        return self.preis

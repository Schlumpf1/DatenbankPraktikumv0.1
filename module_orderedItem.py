class orderedItem:
    def __init__(self,name,preis,count, sum):
        self.name = name
        self.preis = preis
        self.count = count
        self.sum = sum
    def name(self):
        return self.name
    
    def preis(self):
        return self.preis
    
    def count(self):
        return self.count
    
    def sum(self):
        return self.sum
